package dzien1;

public class TypyCalkowite {

  public static void main(String[] args) {
    byte a = 2;
    int b = -3;
    long c = 5;
    long d = 6L; //Longi mozna zapisywać podając L/l na końcu ale nie trzeba
    short e = 6;

    System.out.println(a + " " + b + " " + c + " " + d + " " + e);
  }
}
