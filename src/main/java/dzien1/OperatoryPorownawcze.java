package dzien1;

public class OperatoryPorownawcze {
    public static void main(String[] args) {
        int a = 5;
        int b = 3;
        boolean wynik1 = a > b;
        boolean wynik2 = a < b;
        System.out.println(wynik1 + " " + wynik2);

        boolean wynik3 = 4 >= 4;
        boolean wynik4 = 5 <= 4;
        System.out.println(wynik3 + " " + wynik4);

        boolean wynik5 = a == b;
        boolean wynik6 = a != b;
        System.out.println(wynik5 + " " + wynik6);

    }
}
