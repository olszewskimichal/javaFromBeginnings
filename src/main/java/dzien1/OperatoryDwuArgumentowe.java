package dzien1;

public class OperatoryDwuArgumentowe {
    public static void main(String[] args) {
        int v1 = 3 + 4; // =7​
        int v2 = 4 - 3; // =1​
        int v3 = 3 * 4; // =12​
        int v4 = 3 / 4; // =0​
        int v5 = 3 % 4; // =3

        System.out.println(v1 + " " + v2 + " " + v3 + " " + v4 + " " + v5);

        int liczba1 = 10 + 3 * 2; // =16​
        int liczba2 = (10 + 3) * 2; // =26

        System.out.println(liczba1 + " " + liczba2);
        //zad Reszta z dzielenia przez 3 i czesc calkowita przy dzieleniu przez 3
    }
}
